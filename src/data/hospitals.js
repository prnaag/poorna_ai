export const COST_BANDS = { 1: '₹', 2: '₹₹', 3: '₹₹₹' }

// A few sample doctors per department, generated deterministically so the
// hospital-detail and booking pages always show consistent names/slots.
const FIRST = ['Aarav', 'Vivaan', 'Diya', 'Ananya', 'Kabir', 'Meera', 'Rohan', 'Isha', 'Arjun', 'Neha', 'Aditya', 'Priya']
const LAST = ['Sharma', 'Verma', 'Iyer', 'Reddy', 'Khan', 'Nair', 'Gupta', 'Bose', 'Menon', 'Singh', 'Rao', 'Das']

export function doctorsFor(hospitalId, deptId, n = 3) {
  const seed = (hospitalId + deptId).split('').reduce((s, c) => s + c.charCodeAt(0), 0)
  return Array.from({ length: n }, (_, i) => {
    const f = FIRST[(seed + i * 5) % FIRST.length]
    const l = LAST[(seed + i * 7) % LAST.length]
    const waitOptions = ['Today', 'Today', 'Tomorrow', 'In 2 days']
    return {
      id: `${hospitalId}-${deptId}-${i}`,
      name: `Dr. ${f} ${l}`,
      exp: 8 + ((seed + i * 3) % 22),
      rating: (4 + ((seed + i) % 10) / 10).toFixed(1),
      nextSlot: waitOptions[(seed + i) % waitOptions.length],
    }
  })
}
